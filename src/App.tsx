
import { Toaster } from "@/components/ui/toaster";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Routes, Route } from "react-router-dom";
import { AuthProvider } from "@/hooks/useAuth";
import Index from "./pages/Index";
import Quest from "./pages/Quest";
import LinkCustomizerPage from "./pages/LinkCustomizer";
import PreviewPage from "./pages/PreviewPage";
import UserLinksPage from "./pages/UserLinks";
import NotFound from "./pages/NotFound";
import SubdomainHandler from "./components/SubdomainHandler";

const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      retry: 1,
      refetchOnWindowFocus: false,
    },
  },
});

// Funzione per determinare se siamo su un sottodominio
const isSubdomainEnvironment = () => {
  const hostname = window.location.hostname;
  const pathname = window.location.pathname;
  
  console.log('üöÄ CHECKING SUBDOMAIN - hostname:', hostname);
  console.log('üìÅ CHECKING SUBDOMAIN - pathname:', pathname);
  
  // PRIORIT√Ä 1: Test per sottodomini .lnkfire.dev - SEMPRE vince su tutto
  if (hostname.endsWith('.lnkfire.dev')) {
    const parts = hostname.split('.');
    const subdomain = parts[0];
    
    // Se non √® il dominio principale lnkfire.dev, allora √® un sottodominio
    if (hostname !== 'lnkfire.dev' && hostname !== 'www.lnkfire.dev' && subdomain !== 'www') {
      console.log('‚úÖ MATCHED .lnkfire.dev subdomain - FORCED SUBDOMAIN MODE:', hostname);
      return true;
    }
  }
  
  // PRIORIT√Ä 2: Se siamo sul dominio principale lnkfire.dev (senza sottodominio), NON √® sottodominio
  if (hostname === 'lnkfire.dev' || hostname === 'www.lnkfire.dev') {
    console.log('‚ùå MAIN DOMAIN - NOT SUBDOMAIN:', hostname);
    return false;
  }
  
  // PRIORIT√Ä 3: Ambiente di sviluppo - DISABILITATO per evitare errori
  // Forza sempre la modalit√† normale app invece del sottodominio in sviluppo
  console.log('‚ùå DEVELOPMENT MODE - FORCING NORMAL APP:', hostname);
  return false;
};

const App = () => {
  const isSubdomain = isSubdomainEnvironment();
  
  console.log('üéØ FINAL DECISION - isSubdomain:', isSubdomain);

  // Se siamo su un sottodominio, mostra il SubdomainHandler
  if (isSubdomain) {
    console.log('üé≠ RENDERING SUBDOMAIN HANDLER');
    return (
      <QueryClientProvider client={queryClient}>
        <TooltipProvider>
          <AuthProvider>
            <Toaster />
            <Sonner />
            <SubdomainHandler />
          </AuthProvider>
        </TooltipProvider>
      </QueryClientProvider>
    );
  }

  console.log('üè† RENDERING NORMAL APP');
  // Altrimenti, mostra il normale routing dell'app
  return (
    <QueryClientProvider client={queryClient}>
      <TooltipProvider>
        <AuthProvider>
          <Toaster />
          <Sonner />
          <BrowserRouter>
            <Routes>
              <Route path="/" element={<Index />} />
              <Route path="/quest" element={<Quest />} />
              <Route path="/link-customizer" element={<LinkCustomizerPage />} />
              <Route path="/user-links" element={<UserLinksPage />} />
              <Route path="/preview/:slug" element={<PreviewPage />} />
              <Route path="*" element={<NotFound />} />
            </Routes>
          </BrowserRouter>
        </AuthProvider>
      </TooltipProvider>
    </QueryClientProvider>
  );
};

export default App;
